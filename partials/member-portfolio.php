                <h3>Artist Statement</h3>
                <p>
                As a digital artist and cinematographer based in London, United Kingdom, I navigate the realms of visual storytelling through my creations, blending the rich tapestry of my Nigerian roots with the vibrant, multicultural canvas exploration of Africa. I was born and raised in Nigeria, my artistic journey began with graphic design and evolved into what is now refer to as 'tradigital' art, a fusion of traditional influences and digital techniques.
                </p>
                <p>
                Having pursued a master’s degree in filmmaking from the London Film Academy, my work seamlessly incorporates elements of filmic storytelling into the realm of visual arts. My artistic repertoire spans various forms, including abstract, impressionism, cubism, fauvism, line art, and digital oil paint. These diverse styles serve as my visual language, allowing me to articulate the intricate narratives embedded in Africa's history, tradition, culture, beliefs, values, and daily activities.
                </p>

                <p>
                Drawing inspiration from the dynamic intersection of movie scenes, diverse traditions, and cultural nuances across Africa, I strive to capture the essence of the continent's kaleidoscopic heritage. My art serves as a visual voyage, exploring the multifaceted layers of African identity and the nuanced interplay between the old and the new.
                </p>

                <p>
                
Through the meticulous integration of photography and digital software, I bring forth masterpieces that transcend conventional boundaries. Each stroke and pixel are intentional, weaving together stories that resonate with the viewer's emotions and intellect. My artistic expression, rooted in the 'tradigital' ethos, seeks to bridge the gap between tradition and modernity, echoing the evolving narratives of a continent rich in history and innovation.

                </p>

                <p>
                Whether I delve into abstract realms, create impressionistic masterpiece, or employ cubist forms to deconstruct reality, my art is a celebration of the diversity and resilience embedded in African culture. It is a testament to the power of storytelling through visuals, transcending geographical boundaries to foster a global appreciation for the intricate beauty and complexity of Africa.
                </p>

                <p>

                In essence, my art is a visual symphony, harmonizing the echoes of my Nigerian heritage with the vibrant, cosmopolitan pulse of London. Through the lens of 'tradigital' art, I invite viewers to embark on a journey through time and space, where the past intertwines with the present, and the essence of Africa is illuminated in every brushstroke and pixel.
                </p>

